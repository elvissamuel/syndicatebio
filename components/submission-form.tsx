'use client'

import React from "react"

import { useState, useRef } from 'react'
import { Upload, Loader2 } from 'lucide-react'

interface SubmissionFormProps {
  onSubmit: () => void
}

export function SubmissionForm({ onSubmit }: SubmissionFormProps) {
  const [username, setUsername] = useState('')
  const [message, setMessage] = useState('')
  const [image, setImage] = useState<File | null>(null)
  const [preview, setPreview] = useState<string>('')
  const [selectedFilter, setSelectedFilter] = useState('defiant')
  const [loading, setLoading] = useState(false)
  const [filteredImage, setFilteredImage] = useState<string>('')
  const fileInputRef = useRef<HTMLInputElement>(null)

  const filters = [
    { id: 'defiant', label: 'Defiant', icon: '‚ö°' },
    { id: 'military', label: 'Military', icon: 'üõ°Ô∏è' },
    { id: 'warrior', label: 'Warrior', icon: '‚öîÔ∏è' },
    { id: 'fierce', label: 'Fierce', icon: 'üî•' },
  ]

  const handleImageSelect = (e: React.ChangeEvent<HTMLInputElement>) => {
    const file = e.target.files?.[0]
    if (file) {
      setImage(file)
      const reader = new FileReader()
      reader.onloadend = () => {
        setPreview(reader.result as string)
      }
      reader.readAsDataURL(file)
    }
  }

  const applyFilter = async () => {
    if (!preview) return

    setLoading(true)
    try {
      const response = await fetch('/api/apply-filter', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          imageBase64: preview,
          filterType: selectedFilter,
        }),
      })

      if (response.ok) {
        const data = await response.json()
        setFilteredImage(data.image || preview)
      }
    } catch (error) {
      console.error('[v0] Error applying filter:', error)
    } finally {
      setLoading(false)
    }
  }

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    if (!username || (!message && !preview)) return

    try {
      setLoading(true)
      const response = await fetch('/api/submissions', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          username,
          message,
          imageUrl: filteredImage || preview || null,
          filterApplied: preview ? selectedFilter : 'none',
        }),
      })

      if (response.ok) {
        setUsername('')
        setMessage('')
        setImage(null)
        setPreview('')
        setFilteredImage('')
        setSelectedFilter('defiant')
        onSubmit()
      }
    } catch (error) {
      console.error('[v0] Error submitting:', error)
    } finally {
      setLoading(false)
    }
  }

  return (
    <form onSubmit={handleSubmit} className="bg-white rounded-lg shadow-lg p-6 space-y-4">
      <h2 className="text-2xl font-bold text-gray-900">Share Your Defiance</h2>

      <div>
        <label className="block text-sm font-medium text-gray-700 mb-2">
          Your Name
        </label>
        <input
          type="text"
          value={username}
          onChange={(e) => setUsername(e.target.value)}
          placeholder="Enter your name"
          className="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-red-500 focus:border-transparent"
          required
        />
      </div>

      <div>
        <label className="block text-sm font-medium text-gray-700 mb-2">
          Your Message
        </label>
        <textarea
          value={message}
          onChange={(e) => setMessage(e.target.value)}
          placeholder="Share your affirmation or message (optional if uploading an image)..."
          rows={4}
          className="w-full px-4 py-2 border border-gray-300 rounded-lg focus:ring-2 focus:ring-red-500 focus:border-transparent"
        />
      </div>

      <div>
        <label className="block text-sm font-medium text-gray-700 mb-4">
          Add a Photo (Optional)
        </label>
        <button
          type="button"
          onClick={() => fileInputRef.current?.click()}
          className="w-full border-2 border-dashed border-gray-300 rounded-lg p-6 text-center hover:border-red-500 transition-colors"
        >
          <Upload className="mx-auto mb-2 text-gray-400" size={32} />
          <p className="text-gray-600">Click to upload or drag and drop</p>
        </button>
        <input
          ref={fileInputRef}
          type="file"
          accept="image/*"
          onChange={handleImageSelect}
          className="hidden"
        />
      </div>

      {preview && (
        <div className="space-y-4">
          <div>
            <p className="text-sm font-medium text-gray-700 mb-2">Select Filter</p>
            <div className="grid grid-cols-4 gap-2">
              {filters.map((filter) => (
                <button
                  key={filter.id}
                  type="button"
                  onClick={() => setSelectedFilter(filter.id)}
                  className={`p-3 rounded-lg font-medium transition-all ${
                    selectedFilter === filter.id
                      ? 'bg-red-600 text-white ring-2 ring-red-400'
                      : 'bg-gray-100 text-gray-700 hover:bg-gray-200'
                  }`}
                >
                  <span className="text-xl">{filter.icon}</span>
                  <p className="text-xs mt-1">{filter.label}</p>
                </button>
              ))}
            </div>
          </div>

          <button
            type="button"
            onClick={applyFilter}
            disabled={loading}
            className="w-full bg-blue-600 text-white py-2 rounded-lg hover:bg-blue-700 disabled:bg-gray-400 flex items-center justify-center gap-2"
          >
            {loading && <Loader2 className="animate-spin" size={16} />}
            Apply Filter
          </button>

          <div className="flex gap-4">
            <div className="flex-1">
              <p className="text-xs text-gray-600 mb-2">Original</p>
              <img src={preview || "/placeholder.svg"} alt="Original" className="w-full rounded-lg" />
            </div>
            {filteredImage && (
              <div className="flex-1">
                <p className="text-xs text-gray-600 mb-2">Filtered</p>
                <img src={filteredImage || "/placeholder.svg"} alt="Filtered" className="w-full rounded-lg" />
              </div>
            )}
          </div>
        </div>
      )}

      <button
        type="submit"
        disabled={loading || !username || (!message && !preview)}
        className="w-full bg-red-600 text-white py-3 rounded-lg hover:bg-red-700 disabled:bg-gray-400 font-bold text-lg"
      >
        {loading ? 'Submitting...' : 'Add Your Note'}
      </button>
    </form>
  )
}
